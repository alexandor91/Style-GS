import torch
import torch.nn.functional as F
from torch.autograd import Variable
from math import exp


def l1_loss(network_output, gt):
    # import pdb
    # pdb.set_trace()
    # return torch.abs((network_output - gt))[torch.isinf(torch.abs((network_output - gt)))].mean()
    return torch.abs((network_output - gt)).mean()


def l2_loss(network_output, gt):
    # import pdb
    # pdb.set_trace()
    # network_output - gt
    # torch.square(network_output - gt)
    # return ((network_output - gt) ** 2)[torch.logical_not(torch.isinf(((network_output - gt) ** 2)))].mean()
    return ((network_output - gt) ** 2).mean()


def gaussian(window_size, sigma):
    gauss = torch.Tensor(
        [
            exp(-((x - window_size // 2) ** 2) / float(2 * sigma**2))
            for x in range(window_size)
        ]
    )
    return gauss / gauss.sum()


def create_window(window_size, channel):
    _1D_window = gaussian(window_size, 1.5).unsqueeze(1)
    _2D_window = _1D_window.mm(_1D_window.t()).float().unsqueeze(0).unsqueeze(0)
    window = Variable(
        _2D_window.expand(channel, 1, window_size, window_size).contiguous()
    )
    return window


def ssim(img1, img2, window_size=11, size_average=True):
    channel = img1.size(-3)
    window = create_window(window_size, channel)

    if img1.is_cuda:
        window = window.cuda(img1.get_device())
    window = window.type_as(img1)

    return _ssim(img1, img2, window, window_size, channel, size_average)


def _ssim(img1, img2, window, window_size, channel, size_average=True):
    mu1 = F.conv2d(img1, window, padding=window_size // 2, groups=channel)
    mu2 = F.conv2d(img2, window, padding=window_size // 2, groups=channel)

    mu1_sq = mu1.pow(2)
    mu2_sq = mu2.pow(2)
    mu1_mu2 = mu1 * mu2

    sigma1_sq = (
        F.conv2d(img1 * img1, window, padding=window_size // 2, groups=channel) - mu1_sq
    )
    sigma2_sq = (
        F.conv2d(img2 * img2, window, padding=window_size // 2, groups=channel) - mu2_sq
    )
    sigma12 = (
        F.conv2d(img1 * img2, window, padding=window_size // 2, groups=channel)
        - mu1_mu2
    )

    C1 = 0.01**2
    C2 = 0.03**2

    ssim_map = ((2 * mu1_mu2 + C1) * (2 * sigma12 + C2)) / (
        (mu1_sq + mu2_sq + C1) * (sigma1_sq + sigma2_sq + C2)
    )

    if size_average:
        return ssim_map.mean()
    else:
        return ssim_map.mean(1).mean(1).mean(1)


### stylization loss ###
def style_loss_adain(
    source_feature,
    source_gram,
    target_feature,
    target_gram,
):
    """
    source_feature: [B, C, H, W]
    target_feature: [B, C, H, W]
    """
    style_mean, style_std, content_mean, content_std, style_transformed = adain(
        source_feature, target_feature
    )
    # print("SHAPE AFTER ADAIN: ", style_transformed.shape)
    adain_content_loss = l2_loss(style_mean, content_mean) + l2_loss(
        style_std, content_std
    )
    # G = gram_matrix(style_transformed)
    adain_style_loss = l2_loss(source_gram, target_gram)
    # adain_style_loss = l2_loss(style_transformed, self.style_feature)
    loss = (1 * adain_content_loss) + (10 * adain_style_loss)
    return loss


def gram_matrix(input):
    """
    gram反映channel之间的相关性
    """
    # N C H W
    a, b, c, d = input.size()  # a=batch size(=1)
    # b=number of feature maps
    # (c,d)=dimensions of a f. map (N=c*d)

    features = input.view(a * b, c * d)  # resize F_XL into \hat F_XL

    G = torch.mm(features, features.t())  # compute the gram product

    # we 'normalize' the values of the gram matrix
    # by dividing by the number of element in each feature maps.
    return G.div(a * b * c * d)


def adain(content_feat, style_feat):
    assert content_feat.size()[:2] == style_feat.size()[:2]
    size = content_feat.size()
    style_mean, style_std = calc_mean_std(style_feat)
    content_mean, content_std = calc_mean_std(content_feat)
    normalized_feat = (content_feat - content_mean.expand(size)) / content_std.expand(
        size
    )
    return (
        style_mean,
        style_std,
        content_mean,
        content_std,
        normalized_feat * style_std.expand(size) + style_mean.expand(size),
    )


def calc_mean_std(feat, eps=1e-5):
    # eps is a small value added to the variance to avoid divide-by-zero.
    size = feat.size()
    assert len(size) == 4
    N, C = size[:2]
    feat_var = feat.view(N, C, -1).var(dim=2) + eps
    feat_std = feat_var.sqrt().view(N, C, 1, 1)
    feat_mean = feat.view(N, C, -1).mean(dim=2).view(N, C, 1, 1)
    return feat_mean, feat_std
